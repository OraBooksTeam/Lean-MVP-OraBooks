	var ciDict = {};
	var ciKeys = {}; // pre-sorted key arrays (longest first)
	Object.keys(dict).forEach(function (l) {
		ciDict[l] = {};
		ciKeys[l] = [];
		Object.keys(dict[l]).forEach(function (k) {
			ciDict[l][k.toLowerCase()] = dict[l][k];
			ciKeys[l].push(k.toLowerCase());
		});
		// Sort longest phrase FIRST to prevent partial word matches
		ciKeys[l].sort(function (a, b) { return b.length - a.length; });
	});

	var _textStore = [];

	function savePref(lang) { try { localStorage.setItem('taxora_language', lang); } catch (e) {} }
	function getPref() {
		var lang;
		try { lang = localStorage.getItem('taxora_language'); } catch (e) {}
		if (lang && /^(en|bn|ar)$/.test(lang)) return lang;
		return null;
	}

	function translateAllText(lang) {
		// Always restore English first (prevents cross-language key mismatch)
		_textStore.forEach(function (item) { item.node.textContent = item.orig; });
		_textStore = [];

		if (lang === 'en') return;

		var t = ciDict[lang];
		if (!t) return;
		var walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
			acceptNode: function (node) {
				var p = node.parentNode;
				if (!p) return NodeFilter.FILTER_REJECT;
				var tag = p.nodeName.toLowerCase();
				if (/^(script|style|svg|math|canvas|code|pre)$/.test(tag)) return NodeFilter.FILTER_REJECT;
				if (!node.textContent.trim()) return NodeFilter.FILTER_REJECT;
				return NodeFilter.FILTER_ACCEPT;
			}
		}, false);
		var textNodes = [], n;
		while ((n = walker.nextNode())) textNodes.push(n);
		var keys = ciKeys[lang];
		textNodes.forEach(function (textNode) {
			var result = textNode.textContent;
			var lower = result.toLowerCase().replace(/\s+/g, ' ').trim();
			var changed = false;
			// Longest phrase FIRST — prevents partial word replacement
			keys.forEach(function (key) {
				if (lower.indexOf(key) !== -1) {
					var re = new RegExp(key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'gi');
					result = result.replace(re, t[key]);
					lower = result.toLowerCase().replace(/\s+/g, ' ').trim();
					changed = true;
				}
			});
			if (changed) {
				var found = false;
				for (var i = 0; i < _textStore.length; i++) { if (_textStore[i].node === textNode) { found = true; break; } }
				if (!found) _textStore.push({ node: textNode, orig: textNode.textContent });
				textNode.textContent = result;
			}
		});
	}

	function updateTopbar(lang) {
		var btn = document.getElementById('taxora-lang-btn');
		if (btn) btn.textContent = (langLabels[lang] || 'English') + ' ▾';
		document.querySelectorAll('#taxora-language-menu a[data-lang]').forEach(function (a) {
			a.classList.remove('active', 'selected');
		});
		var active = document.querySelector('#taxora-language-menu a[data-lang="' + lang + '"]');
		if (active) active.classList.add('active', 'selected');
		document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
		document.documentElement.lang = lang;
	}

	function applyLanguage(lang) {
		try {
			if (!lang || !langLabels[lang]) lang = 'en';
			savePref(lang);
			updateTopbar(lang);
			translateAllText(lang);
			var xhr = new XMLHttpRequest();
			xhr.open('POST', V.ajaxUrl || '/wp-admin/admin-ajax.php', true);
			xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
			xhr.send('action=taxora_update_user_language&lang=' + encodeURIComponent(lang) + '&nonce=' + encodeURIComponent(V.langNonce || ''));
		} catch (e) {}
	}

	function closeAll() {
		document.querySelectorAll('#taxora-language-menu.show, #taxora-settings-menu.show, #taxora-calendar.show').forEach(function (o) { o.classList.remove('show'); });
	}

	document.addEventListener('click', function (e) {
		var t = e.target;
		if (t.closest('#taxora-lang-btn')) {
			var m = document.getElementById('taxora-language-menu');
			if (m) { m.classList.toggle('show'); ['taxora-settings-menu', 'taxora-calendar'].forEach(function (id) { var el = document.getElementById(id); if (el) el.classList.remove('show'); }); }
			return;
		}
		if (t.closest('#taxora-settings-btn')) {
			var sm = document.getElementById('taxora-settings-menu');
			if (sm) { sm.classList.toggle('show'); ['taxora-language-menu', 'taxora-calendar'].forEach(function (id) { var el = document.getElementById(id); if (el) el.classList.remove('show'); }); }
			return;
		}
		var ll = t.closest('#taxora-language-menu a[data-lang]');
		if (ll) {
			e.preventDefault();
			var lang = ll.getAttribute('data-lang');
			if (!langLabels[lang]) return;
			var menu = document.getElementById('taxora-language-menu');
			if (menu) menu.classList.remove('show');
			applyLanguage(lang);
			return;
		}
		if (!t.closest('.taxora-dropdown') && !t.closest('.taxora-calendar-btn') && !t.closest('#taxora-calendar')) closeAll();
	});

	function initLang() {
		if (!document.getElementById('taxora-topbar')) { setTimeout(initLang, 100); return; }
		var saved = getPref();
		if (saved) {
			updateTopbar(saved);
			if (saved !== 'en') translateAllText(saved);
		}
	}

	var dynamicObserver = new MutationObserver(function () {
		var saved = getPref();
		if (saved && saved !== 'en') translateAllText(saved);
	});

	if (document.readyState === 'loading') {
		document.addEventListener('DOMContentLoaded', function () {
			initLang();
			dynamicObserver.observe(document.body, { childList: true, subtree: true });
		});
	} else {
		initLang();
		dynamicObserver.observe(document.body, { childList: true, subtree: true });
	}

	var obs = new MutationObserver(function () {
		if (document.getElementById('taxora-topbar')) { obs.disconnect(); initLang(); }
	});
	obs.observe(document.documentElement, { childList: true, subtree: true });

